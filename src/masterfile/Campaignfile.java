private void checkuserconfig() {
        //MenuName = Mast1
        UserLogin userrec = UserLogin.userlogin;
        if (userrec.getMast15().equals("1")) {
            btnSave.setEnabled(false);
            btnDetete.setEnabled(false);
            menuSave.setEnabled(false);
            menuDelete.setEnabled(false);
        }
        if (userrec.getMast15().equals("2")) {
            btnSave.setEnabled(true);
            btnDetete.setEnabled(false);
            menuSave.setEnabled(true);
            menuDelete.setEnabled(false);
        }
        if (userrec.getMast15().equals("3")) {
            btnSave.setEnabled(true);
            btnDetete.setEnabled(true);
            menuSave.setEnabled(true);
            menuDelete.setEnabled(true);
        }
    }
    private void tblBtypeMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBtypeMouseReleased
        if (evt.getClickCount() == 2) {
            int index = tblBtype.getSelectedRow();
            String typecode = tblBtype.getValueAt(index, 0).toString();
            String typedis = tblBtype.getValueAt(index, 1).toString();
            if (txtfCode.getText().equals("")) {
                txtfCode.setText(typecode);
                txtfDiscription.setText(typedis);
               
            } else if (!txtfCode.getText().equals(typecode)) {
                int confirm = JOptionPane.showConfirmDialog(this, "คุณมีการเพิ่มหรือแก้ไขข้อมูลอยู่ คุณต้องการยกเลิกการเพิ่มหรือแก้ไขข้อมูลนั้นหรือไม่", "Confirm", JOptionPane.YES_NO_OPTION);
                if (confirm == JOptionPane.YES_OPTION) {
                    txtfCode.setText(typecode);
                    txtfDiscription.setText(typedis);
                    
                }
            }
            setFocus(true);
            txtfDiscription.requestFocus();
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_tblBtypeMouseReleased

