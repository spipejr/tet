    private void sap_codeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_sap_codeKeyPressed
        if (evt.getKeyCode() == evt.VK_ENTER) {
            if (sap_code.getText().trim().length() > 0) {
                String sap_site_code = sap_code.getText();
                _site = _site.seekSiteMaster(sap_site_code);
                if (_site.haveSite()) {
                    if (!dbUtility.Char_Set.equalsIgnoreCase("utf-8")) {
                        sap_name.setText(cvth.ASCII2Unicode(_site.getSap_name()));
                        sap_costcenter.setText(cvth.ASCII2Unicode(_site.getSap_costcenter()));
                        sap_address.setText(cvth.ASCII2Unicode(_site.getSap_address()));
                        sap_locality.setText(cvth.ASCII2Unicode(_site.getSap_locality()));
                    } else {
                        sap_name.setText(_site.getSap_name());
                        sap_costcenter.setText(_site.getSap_costcenter());
                        sap_address.setText(_site.getSap_address());
                        sap_locality.setText(_site.getSap_locality());
                    }
                } else {
                    msn.showMessageInformation("ไม่พบรหัส Site Code ที่ระบุ ...");
                    sap_code.setText("");
                    sap_name.setText("");
                    sap_costcenter.setText("");
                    sap_address.setText("");
                    sap_locality.setText("");
                    us.openTextFiled(sap_code, false, true);
                }
            }
        } else if (evt.getKeyCode() == evt.VK_F1) {
            btn_sitemasterActionPerformed(null);
        } else if (evt.getKeyCode() == evt.VK_ESCAPE) {
            us.openTextFiled(sap_code, true, true);
            us.closeTextFiled(sap_name, true);
            us.closeTextFiled(sap_costcenter, true);
            us.closeTextFiled(sap_address, true);
            us.closeTextFiled(sap_locality, true);
        }
    }//GEN-LAST:event_sap_codeKeyPressed

