    private void deleteuser() {
        int index = tbluser.getSelectedRow();
        String creditcode = tbluser.getValueAt(index, 0).toString();
        if (!creditcode.equals("")) {
            try {
                int confirm = JOptionPane.showConfirmDialog(this, "คุณต้องการลบข้อมูลรายการนี้หรือไม่...?", "Confirm", JOptionPane.YES_NO_OPTION);
                if (confirm == JOptionPane.YES_OPTION) {
                    if (!dbUtility.Char_Set.equalsIgnoreCase("utf-8")) {
                        stmt = (Statement) dbUtility.con.createStatement();
                        String sql = "Delete from branvoid  where u_code = ? and b_code=?";
                        PreparedStatement prm = (PreparedStatement) dbUtility.con.prepareStatement(sql);
                        prm.setString(1, cvth.Unicode2ASCII(creditcode));
                        prm.setString(2, cvth.Unicode2ASCII(t_brancode.getText()));
                        prm.executeUpdate();
                        stmt.close();
                    } else {
                        stmt = (Statement) dbUtility.con.createStatement();
                        String sql = "Delete from branvoid  where u_code = ? and b_code=?";
                        PreparedStatement prm = (PreparedStatement) dbUtility.con.prepareStatement(sql);
                        prm.setString(1, creditcode);
                        prm.setString(2, t_brancode.getText());
                        prm.executeUpdate();
                        stmt.close();
                    }
                    //Delete From Branch_void
                    loadvoiduser();
                }
            } catch (Exception e) {
            }
        }
    }

    private void savevoid() {
        if (!chkbvoid(t_code1.getText())) {
            try {
                if (!dbUtility.Char_Set.equalsIgnoreCase("utf-8")) {
                    stmt = (Statement) dbUtility.con.createStatement();
                    String sql = "Insert into branvoid (u_code,b_code) "
                            + " values(?,?)";
                    PreparedStatement prm = (PreparedStatement) dbUtility.con.prepareStatement(sql);
                    prm.setString(1, cvth.Unicode2ASCII(t_code1.getText().trim()));
                    prm.setString(2, cvth.Unicode2ASCII(t_brancode.getText()));
                    prm.executeUpdate();
                } else {
                    stmt = (Statement) dbUtility.con.createStatement();
                    String sql = "Insert into branvoid (u_code,b_code) "
                            + " values(?,?)";
                    PreparedStatement prm = (PreparedStatement) dbUtility.con.prepareStatement(sql);
                    prm.setString(1, t_code1.getText().trim());
                    prm.setString(2, t_brancode.getText());
                    prm.executeUpdate();
                }
                stmt.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Insert Void User Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        loadvoiduser();
        t_code1.setText("");
        t_code1.requestFocus();
    }

