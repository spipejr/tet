    public void loadvoiduser() {
        stmt = null;
        ResultSet rs = null;
        tbCreditCardSetup = (DefaultTableModel) tbluser.getModel();
        tbluser.setShowGrid(true);
        tbluser.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        tbluser.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        tbluser.setRowSelectionAllowed(true);
        tbluser.setBackground(java.awt.Color.WHITE);
        tbluser.setGridColor(java.awt.Color.gray);
        JTableHeader header = tbluser.getTableHeader();
        header.setFont(new java.awt.Font("Norasi", java.awt.Font.BOLD, 14));
        tbluser.setRowHeight(25);
        tbluser.setAutoCreateRowSorter(true);
        int cnt = tbCreditCardSetup.getRowCount();
        if (cnt > 0) {
            for (int i = 0; i < cnt; i++) {
                tbCreditCardSetup.removeRow(0);
            }
        }
        try {
            stmt = (Statement) dbUtility.con.createStatement();
            String sql = "select * from branvoid "
                    + "left join voiduser on branvoid.u_code=voiduser.u_code "
                    + "where b_code='" + t_brancode.getText() + "' "
                    + "order by branvoid.u_code";
            rs = stmt.executeQuery(sql);
            while (rs.next()) {
                {
                    if (!dbUtility.Char_Set.equalsIgnoreCase("utf-8")) {
                        tbCreditCardSetup.addRow(new Object[]{cvth.ASCII2Unicode(rs.getString("u_code")),
                            cvth.ASCII2Unicode(rs.getString("u_name")),
                            cvth.ASCII2Unicode(rs.getString("u_position")),
                            cvth.ASCII2Unicode(rs.getString("u_email")),
                            cvth.ASCII2Unicode(rs.getString("u_phone"))
                        });
                    } else {
                        tbCreditCardSetup.addRow(new Object[]{rs.getString("u_code"),
                            rs.getString("u_name"),
                            rs.getString("u_position"),
                            rs.getString("u_email"),
                            rs.getString("u_phone")
                        });
                    }
                }
            }
        } catch (Exception e) {
        }
    }

