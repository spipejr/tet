    public void GetData(String Code) {
        String credit = "";
        if (CheckValidEnter()) {
            try {
                String sql = "select * from voiduser where u_code='" + Code + "'";

                stmt = (Statement) dbUtility.con.createStatement();
                ResultSet rs = stmt.executeQuery(sql);
                if (rs.next()) {
                    if (!dbUtility.Char_Set.equalsIgnoreCase("utf-8")) {
                        t_code.setText(cvth.ASCII2Unicode(rs.getString("u_code")));
                        t_name.setText(cvth.ASCII2Unicode(rs.getString("u_name")));
                        t_position.setText(cvth.ASCII2Unicode(rs.getString("u_position")));
                        t_email.setText(cvth.ASCII2Unicode(rs.getString("u_email")));
                        t_phone.setText(cvth.ASCII2Unicode(rs.getString("u_phone")));
                    } else {
                        t_code.setText(rs.getString("u_code"));
                        t_name.setText(rs.getString("u_name"));
                        t_position.setText(rs.getString("u_position"));
                        t_email.setText(rs.getString("u_email"));
                        t_phone.setText(rs.getString("u_phone"));
                    }
                } else {
                    t_name.requestFocus();
                }
                rs.close();
                stmt.close();
                setActive();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }
        }
    }

