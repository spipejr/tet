    private void txtbcodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtbcodeKeyPressed
        try {
            if (evt.getKeyCode() == KeyEvent.VK_ESCAPE) {
                clearall();
                setComponentEnable(false);
                txtbcode.setText("");
                txtbname.setText("");
                txtbcode.requestFocus();
            } else if (evt.getKeyCode() == KeyEvent.VK_F5) {
                
                
            } else if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
                if (!txtbcode.getText().trim().equals("")) {    
                    if (setbgroupName(txtbcode.getText())) {
                        if (chkpos(txtbcode.getText())) {
                            setComponentEnable(true);
                            txtvat.requestFocus();
                        } else {
                            cleartab();
                            setComponentEnable(true);
                            txtvat.requestFocus();
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "ข้อมูลไม่ถูกต้อง กรุณากรอกข้อมูลให้ถูกต้อง");
                    }
                    
                }
                
            } else if (evt.getKeyCode() == KeyEvent.VK_F1) {
                btnsearcbgroup();
            }            
        } catch (Exception ex) {
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_txtbcodeKeyPressed
    
