     public boolean CheckValidSave() {
        boolean chk = true;


        if (t_mcode.getText().length() > 20) {
            JOptionPane.showMessageDialog(this, "กำหนดข้อมูลไม่ถูกต้อง... (ต้องมีขนาดไม่เกิน 20 ตัวอักษร)");
            chk = false;
            t_mcode.requestFocus();
        } else if (t_pcode.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "กรุณากำหนด รายละเอียด Option");
            chk = false;
            t_pcode.requestFocus();
        } else if (t_no.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "กรุณากำหนดลำดับ");
            chk = false;
            t_no.requestFocus();
        }
        else if(chkAll(t_mcode.getText(),t_pcode.getText()))
        {
            JOptionPane.showMessageDialog(this, "ไม่สามารถเพิ่มข้อมูลได้ เนื่องจากมีข้อมูลอยู่ในกลุ่มนี้แล้ว");
                    t_pcode.setText("");
                    t_pcode.requestFocus();
                    chk = false;
        }
        
        
        int no = Integer.parseInt(t_no.getText());
        if(no < 0 || no >999)
        {
            JOptionPane.showMessageDialog(this, "กำหนดข้อมูลไม่ถูกต้อง... (ลำดับห้ามน้อยกว่า 0 และมากกว่า 999)");
            chk = false;
            t_no.requestFocus();
        }
        return chk;
    }
