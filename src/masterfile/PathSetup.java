    private boolean chkvalidate() {
        boolean chk = true;
        if (txtfBackupPath.getText().length() > 30) {
            JOptionPane.showMessageDialog(this, "กำหนด BackupPath ไม่ถูกต้อง... (ต้องมีขนาดไม่เกิน 30 ตัวอักษร)");
            chk = false;
            txtfBackupPath.requestFocus();
        } else if (txtfDataPath.getText().length() > 30) {
            JOptionPane.showMessageDialog(this, "กำหนด DataPath ไม่ถูกต้อง... (ต้องมีขนาดไม่เกิน 30 ตัวอักษร)");
            chk = false;
            txtfDataPath.requestFocus();
        } else if (txtfServerPath.getText().length() > 50) {
            JOptionPane.showMessageDialog(this, "กำหนด ServerPath ไม่ถูกต้อง... (ต้องมีขนาดไม่เกิน 50 ตัวอักษร)");
            chk = false;
            txtfServerPath.requestFocus();
        } else if (txtfServerPathFG.getText().length() > 45) {
            JOptionPane.showMessageDialog(this, "กำหนด ServerPathFG ไม่ถูกต้อง... (ต้องมีขนาดไม่เกิน 45 ตัวอักษร)");
            chk = false;
            txtfServerPathFG.requestFocus();
        } else if (txtfBackupPath1.getText().length() > 30) {
            JOptionPane.showMessageDialog(this, "กำหนด BackupPath ไม่ถูกต้อง... (ต้องมีขนาดไม่เกิน 30 ตัวอักษร)");
            chk = false;
            txtfBackupPath1.requestFocus();
        } else if (txtfDataPath1.getText().length() > 30) {
            JOptionPane.showMessageDialog(this, "กำหนด DataPath ไม่ถูกต้อง... (ต้องมีขนาดไม่เกิน 30 ตัวอักษร)");
            chk = false;
            txtfDataPath1.requestFocus();
        } else if (txtfServerPath1.getText().length() > 50) {
            JOptionPane.showMessageDialog(this, "กำหนด ServerPath ไม่ถูกต้อง... (ต้องมีขนาดไม่เกิน 50 ตัวอักษร)");
            chk = false;
            txtfServerPath1.requestFocus();
        } else if (txtfServerPathFG1.getText().length() > 45) {
            JOptionPane.showMessageDialog(this, "กำหนด ServerPathFG ไม่ถูกต้อง... (ต้องมีขนาดไม่เกิน 45 ตัวอักษร)");
            chk = false;
            txtfServerPathFG1.requestFocus();
        }
        return chk;
    }

