    public void btnsave() {
        String _usesap = "N";
        if (chkSAP.isSelected()) {
            _usesap = "Y";
        }

        if (chkvalidate()) {

            try {
                if (!db.Char_Set.equalsIgnoreCase("utf-8")) {
                    stmt = (Statement) db.con.createStatement();
                    String sql = "Update borpathsetup set BackUpPath = ?,DataPath = ?,ServerPath = ?,ServerPathFG = ?,BackUpPath_w = ?,DataPath_w = ?,ServerPath_w = ?,ServerPathFG_w = ?,"
                            + "path_sap_article=?,  path_sap_site=?, use_sap_article=?, path_resultsap=?, path_resultsap_backup=? ";
                    PreparedStatement prm = (PreparedStatement) db.con.prepareStatement(sql);
                    prm.setString(1, cvth.Unicode2ASCII(txtfBackupPath.getText()));
                    prm.setString(2, cvth.Unicode2ASCII(txtfDataPath.getText()));
                    prm.setString(3, cvth.Unicode2ASCII(txtfServerPath.getText()));
                    prm.setString(4, cvth.Unicode2ASCII(txtfServerPathFG.getText()));
                    prm.setString(5, cvth.Unicode2ASCII(txtfBackupPath1.getText()));
                    prm.setString(6, cvth.Unicode2ASCII(txtfDataPath1.getText()));
                    prm.setString(7, cvth.Unicode2ASCII(txtfServerPath1.getText()));
                    prm.setString(8, cvth.Unicode2ASCII(txtfServerPathFG1.getText()));
                    prm.setString(9, cvth.Unicode2ASCII(txt_sap_article.getText()));
                    prm.setString(10, cvth.Unicode2ASCII(txt_sap_site.getText()));
                    prm.setString(11, cvth.Unicode2ASCII(_usesap));
                    prm.setString(12, cvth.Unicode2ASCII(txt_sap_result.getText()));
                    prm.setString(13, cvth.Unicode2ASCII(txt_sap_resultbackup.getText()));
                    prm.executeUpdate();

                } else {
                    stmt = (Statement) db.con.createStatement();
                    String sql = "Update borpathsetup set BackUpPath = ?,DataPath = ?,ServerPath = ?,ServerPathFG = ?,BackUpPath_w = ?,DataPath_w = ?,ServerPath_w = ?,ServerPathFG_w = ?,"
                            + "path_sap_article=?,  path_sap_site=?,  use_sap_article=?, path_resultsap=?, path_resultsap_backup=? ";
                    PreparedStatement prm = (PreparedStatement) db.con.prepareStatement(sql);
                    prm.setString(1, txtfBackupPath.getText());
                    prm.setString(2, txtfDataPath.getText());
                    prm.setString(3, txtfServerPath.getText());
                    prm.setString(4, txtfServerPathFG.getText());
                    prm.setString(5, txtfBackupPath1.getText());
                    prm.setString(6, txtfDataPath1.getText());
                    prm.setString(7, txtfServerPath1.getText());
                    prm.setString(8, txtfServerPathFG1.getText());
                    prm.setString(9, txt_sap_article.getText());
                    prm.setString(10, txt_sap_site.getText());
                    prm.setString(11, _usesap);
                    prm.setString(12, (txt_sap_result.getText()));
                    prm.setString(13, (txt_sap_resultbackup.getText()));
                    prm.executeUpdate();
                }
                stmt.close();
                clear();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Update Error", JOptionPane.ERROR_MESSAGE);
            }

        }

    }

