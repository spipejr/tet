private void showdata() {
    stmt = null;
    ResultSet rs = null;
    
   
    tbPromotion = (DefaultTableModel) tblPromotion.getModel();
     int cnt = tbPromotion.getRowCount();
        if (cnt > 0) {
            for (int i = 0; i < cnt; i++) {
                tbPromotion.removeRow(0);
            }
        }
    try {
        stmt = (Statement) dbUtility.con.createStatement();
        String sql = "select * from protab order by procode";
        rs = stmt.executeQuery(sql);
        while(rs.next())
        {
            if(!db.Char_Set.equalsIgnoreCase("utf-8"))
            {
            tbPromotion.addRow(new Object[]{cvth.ASCII2Unicode(rs.getString("procode")),
                                            cvth.ASCII2Unicode(rs.getString("prodesc")),
                                            cvth.ASCII2Unicode(dateFmtShow.format(rs.getDate("pdate1"))),
                                            cvth.ASCII2Unicode(dateFmtShow.format(rs.getDate("pdate2"))),
                                            cvth.ASCII2Unicode(rs.getString("pstrday")),
                                            cvth.ASCII2Unicode(rs.getString("ptype"))            
                                           });
            }
            else
            {
            tbPromotion.addRow(new Object[]{rs.getString("procode"),
                                            rs.getString("prodesc"),
                                            dateFmtShow.format(rs.getDate("pdate1")),
                                            dateFmtShow.format(rs.getDate("pdate2")),
                                            rs.getString("pstrdate"),
                                           rs.getString("ptype")            
                                           });
            }
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e);
    }
    }
