private void showdata() {
    stmt = null;
    ResultSet rs = null;
    
   
    tbCoupon = (DefaultTableModel) tblCoupon.getModel();
     int cnt = tbCoupon.getRowCount();
        if (cnt > 0) {
            for (int i = 0; i < cnt; i++) {
                tbCoupon.removeRow(0);
            }
        }
    try {
        stmt = (Statement) dbUtility.con.createStatement();
        String sql = "select * from propoint order by procode";
        rs = stmt.executeQuery(sql);
        while(rs.next())
        {
            if(!db.Char_Set.equalsIgnoreCase("utf-8"))
            {
            tbCoupon.addRow(new Object[]{cvth.ASCII2Unicode(rs.getString("procode")),
                                      cvth.ASCII2Unicode(rs.getString("proname")),
                                      cvth.ASCII2Unicode(dateFmtShow.format(rs.getDate("probegin"))),
                                      cvth.ASCII2Unicode(dateFmtShow.format(rs.getDate("proend"))),
                                      cvth.ASCII2Unicode(rs.getString("prostrday")),
                                      cvth.ASCII2Unicode(rs.getString("prosaletype"))        
                                           });
            }
            else
            {
            tbCoupon.addRow(new Object[]{rs.getString("procode"),
                                            rs.getString("proname"),
                                            dateFmtShow.format(rs.getDate("probegin")),
                                            dateFmtShow.format(rs.getDate("proend")),
                                            rs.getString("prostrday"),
                                           rs.getString("prosaletype")            
                                           });
            }
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e);
    }
    }
