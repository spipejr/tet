    public BranchCodeName getRecordAt(String GroupCode) {
        ResultSet rs;
        String sql;
        BranchCodeName fb = new BranchCodeName();

        try {
            sql = "SELECT * FROM voidconfig WHERE v_code='" + GroupCode + "'";
            stmt = dbUtility.con.createStatement();
            rs = stmt.executeQuery(sql);
            if (rs != null) {

                while (rs.next()) {
                    if (!dbUtility.Char_Set.equalsIgnoreCase("utf-8")) {
                        fb.setCode(cvth.ASCII2Unicode(rs.getString("v_code")));
                        fb.setName(cvth.ASCII2Unicode(rs.getString("v_name")));
                    } else {
                        fb.setCode(rs.getString("v_code"));
                        fb.setName(rs.getString("v_name"));
                    }

                }
                return fb;

            } else {
                return null;
            }
        } catch (SQLException ex) {
            return null;
        }
    }
