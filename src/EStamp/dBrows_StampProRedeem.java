    private void resetTable() {
        int rowCount = model.getRowCount();
        for (int i = 0; i < rowCount; i++) {
            model.removeRow(0);
        }
    }

    private void setTable() {
        model = (DefaultTableModel) tblStampPro.getModel();
        tblStampPro.setShowGrid(true);
        tblStampPro.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        tblStampPro.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        tblStampPro.setRowSelectionAllowed(true);
        tblStampPro.setShowGrid(true);
        tblStampPro.setBackground(Color.WHITE);
        tblStampPro.setGridColor(Color.LIGHT_GRAY);

        JTableHeader header = tblStampPro.getTableHeader();
        header.setFont(new java.awt.Font("Norasi", java.awt.Font.BOLD, 14));
        tblStampPro.setFont(new java.awt.Font("Norasi", java.awt.Font.PLAIN, 14));
        tblStampPro.setRowHeight(25);

        ((DefaultTableCellRenderer) tblStampPro.getTableHeader().getDefaultRenderer()).setHorizontalAlignment(JLabel.CENTER);

        DecimalFormat DoubleFmt = new DecimalFormat("##,###,##0.00");
        DecimalFormat IntegerFmt = new DecimalFormat("##,###,##0");
        DecimalFormat PersentFmt = new DecimalFormat("#,##0.00%");

        TableColumnModel tcm = tblStampPro.getColumnModel();

        DefaultTableCellRenderer d;

        d = new DefaultTableCellRenderer();
        d.setHorizontalAlignment(SwingConstants.CENTER);
        tcm.getColumn(0).setCellRenderer(d);

        d = new DefaultTableCellRenderer();
        d.setHorizontalAlignment(SwingConstants.LEFT);
        tcm.getColumn(1).setCellRenderer(d);

        d = new DefaultTableCellRenderer();
        d.setHorizontalAlignment(SwingConstants.RIGHT);
    }

    private void loadData() {
        resetTable();
        try {
            String sql = "SELECT sr_code, sr_name FROM  stampredeem order by sr_code";
            pre = dbUtility.con.prepareStatement(sql);
            rs = pre.executeQuery();
            rs.first();
            if (rs.getRow() != 0) {
                if (!db.Char_Set.equalsIgnoreCase("utf-8")) {
                    do {
                        model.addRow(new Object[]{
                            cvth.ASCII2Unicode(rs.getString("sr_code")),
                            cvth.ASCII2Unicode(rs.getString("sr_name"))
                        });
                    } while (rs.next());
                } else {
                    do {
                        model.addRow(new Object[]{
                            rs.getString("sr_code"),
                            rs.getString("sr_name")
                        });
                    } while (rs.next());
                }
            }
            if (tblStampPro.getRowCount() > 0) {
                tblStampPro.setFocusable(true);
                tblStampPro.setRowSelectionInterval(0, 0);
                tblStampPro.requestFocus();
            }
        } catch (Exception e) {
            e.printStackTrace();
            msn.showMessageError(e.getMessage());
        }
    }

