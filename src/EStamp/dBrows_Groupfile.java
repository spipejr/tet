    private void resetTable() {
        int rowCount = model.getRowCount();
        for (int i = 0; i < rowCount; i++) {
            model.removeRow(0);
        }
    }

    private void setTable() {
        model = (DefaultTableModel) tbl_groupfile.getModel();
        tbl_groupfile.setShowGrid(true);
        tbl_groupfile.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        tbl_groupfile.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        tbl_groupfile.setRowSelectionAllowed(true);
        tbl_groupfile.setShowGrid(true);
        tbl_groupfile.setBackground(Color.WHITE);
        tbl_groupfile.setGridColor(Color.LIGHT_GRAY);

        JTableHeader header = tbl_groupfile.getTableHeader();
        header.setFont(new java.awt.Font("Norasi", java.awt.Font.BOLD, 14));
        tbl_groupfile.setFont(new java.awt.Font("Norasi", java.awt.Font.PLAIN, 14));
        tbl_groupfile.setRowHeight(25);

        ((DefaultTableCellRenderer) tbl_groupfile.getTableHeader().getDefaultRenderer()).setHorizontalAlignment(JLabel.CENTER);

        DecimalFormat DoubleFmt = new DecimalFormat("##,###,##0.00");
        DecimalFormat IntegerFmt = new DecimalFormat("##,###,##0");
        DecimalFormat PersentFmt = new DecimalFormat("#,##0.00%");

        TableColumnModel tcm = tbl_groupfile.getColumnModel();

        DefaultTableCellRenderer d;

        d = new DefaultTableCellRenderer();
        d.setHorizontalAlignment(SwingConstants.CENTER);
        tcm.getColumn(0).setCellRenderer(d);

        d = new DefaultTableCellRenderer();
        d.setHorizontalAlignment(SwingConstants.LEFT);
        tcm.getColumn(1).setCellRenderer(d);

        d = new DefaultTableCellRenderer();
        d.setHorizontalAlignment(SwingConstants.RIGHT);
    }

    private void loadData() {
        resetTable();

        try {
            String sql = "SELECT * FROM groupfile order by groupcode";
            pre = dbUtility.con.prepareStatement(sql);
            rs = pre.executeQuery();
            rs.first();
            if (rs.getRow() != 0) {
                if (!db.Char_Set.equalsIgnoreCase("utf-8")) {
                    do {
                        model.addRow(new Object[]{
                            cvth.ASCII2Unicode(rs.getString("groupcode")),
                            cvth.ASCII2Unicode(rs.getString("groupname"))
                        });
                    } while (rs.next());
                } else {
                    do {
                        model.addRow(new Object[]{
                            rs.getString("groupcode"),
                            rs.getString("groupname")
                        });
                    } while (rs.next());
                }

            }
            if (tbl_groupfile.getRowCount() > 0) {
                tbl_groupfile.setFocusable(true);
                tbl_groupfile.setRowSelectionInterval(0, 0);
                tbl_groupfile.requestFocus();
            }
        } catch (Exception e) {
            e.printStackTrace();
            msn.showMessageError(e.getMessage());
        } finally {
            try {
                if (pre != null) {
                    pre.close();
                    pre = null;
                }
                if (rs != null) {
                    rs.close();
                    rs = null;
                }
            } catch (Exception e) {
            }
        }
    }

