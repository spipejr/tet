    private void ProcessSaveExcel(JTable table) {
        modelExcel = (DefaultTableModel) table.getModel();
        String strFrom = txtSFrom.getText().trim();
        String strTo = txtSTo.getText().trim();
        String strDate1 = txtDateStr1.getText();
        String strDate2 = txtDateStr2.getText();

        if (strTo.equals("")) {
            strTo = "ZZZ";
        }

        if (strDate1.equals("")) {
            msn.showMessageWarning("กรุณาระบุวันที่ให้ถูกต้อง !!!");
            txtDateStr1.requestFocus();
        } else {
            strDate1 = f.convertDateshowToSQL(strDate1);
        }

        if (strDate2.equals("")) {
            msn.showMessageWarning("กรุณาระบุวันที่ให้ถูกต้อง !!!");
            txtDateStr2.requestFocus();
        } else {
            strDate2 = f.convertDateshowToSQL(strDate2);
        }

        try {
            String sql = "select * from stampcampaign where s_campaigncode >='" + strFrom + "' and s_campaigncode <='" + strTo + "' "
                    + "and s_campaignstart >='" + strDate1 + "' and s_campaignstart <='" + strDate2 + "' ";
            System.out.println("ProcessSaveExcel // " + sql);
            pre = dbUtility.con.prepareStatement(sql);
            rs = pre.executeQuery();
            rs.first();
            if (rs.getRow() != 0) {
                if (!db.Char_Set.equalsIgnoreCase("utf-8")) {
                    do {
                        modelExcel.addRow(new Object[]{
                            cvth.ASCII2Unicode(rs.getString("s_campaigncode")),
                            cvth.ASCII2Unicode(rs.getString("s_campaignname")),
                            cvth.ASCII2Unicode(f.convertDatesqlToShow(rs.getString("s_campaignstart"))),
                            cvth.ASCII2Unicode(f.convertDatesqlToShow(rs.getString("s_campaignend"))),
                            cvth.ASCII2Unicode(rs.getString("s_campaignremark"))
                        });
                    } while (rs.next());
                } else {
                    do {
                        modelExcel.addRow(new Object[]{
                            rs.getString("s_campaigncode"),
                            rs.getString("s_campaignname"),
                            f.convertDatesqlToShow(rs.getString("s_campaignstart")),
                            f.convertDatesqlToShow(rs.getString("s_campaignend")),
                            rs.getString("s_campaignremark")
                        });
                    } while (rs.next());
                }
                saveExcel(table);
                clearForm();
            } else {
                msn.showMessageInformation("ไม่พบข้อมูลตามเงื่อนไขที่ระบุ ...");
            }
        } catch (Exception e) {
            e.printStackTrace();
            msn.showMessageError(e.getMessage());
        }
    }

