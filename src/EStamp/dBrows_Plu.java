    private void btn_chkallActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_chkallActionPerformed
        int cnt = model.getRowCount();
        if (cnt > 0) {
            for (int i = 0; i < cnt; i++) {
                model.removeRow(0);
            }
        }
        try {
            String sql = "select * from product left join groupfile on pgroup=groupcode "
                    + "order by pcode";
            stmt = dbUtility.con.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            while (rs.next()) {

                model.addRow(new Object[]{
                    true,
                    rs.getString("pgroup") + " " + rs.getString("groupname"),
                    rs.getString("pcode"),
                    rs.getString("pdesc"),
                    rs.getDouble("pprice11"),
                    rs.getString("punit1"),
                    rs.getString("pactive")
                });

            }
            rs.close();
            stmt.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_btn_chkallActionPerformed

    private void btn_unchkallActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_unchkallActionPerformed
        int cnt = model.getRowCount();
        if (cnt > 0) {
            for (int i = 0; i < cnt; i++) {
                model.removeRow(0);
            }
        }
        try {
            String sql = "select * from product left join groupfile on pgroup=groupcode "
                    + "order by pcode";
            stmt = dbUtility.con.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            while (rs.next()) {

                model.addRow(new Object[]{
                    false,
                    cvth.ASCII2Unicode(rs.getString("pgroup") + " " + rs.getString("groupname")),
                    cvth.ASCII2Unicode(rs.getString("pcode")),
                    cvth.ASCII2Unicode(rs.getString("pdesc")),
                    rs.getDouble("pprice11"),
                    cvth.ASCII2Unicode(rs.getString("punit1")),
                    cvth.ASCII2Unicode(rs.getString("pactive"))
                });

            }
            rs.close();
            stmt.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_btn_unchkallActionPerformed

