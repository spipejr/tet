    private void clearModel() {
        int size = tbltable.getRowCount();
        for (int i = 0; i < size; i++) {
            model.removeRow(0);
        }
    }

    private void showtable(String where, String code) {
        String record = com_record.getSelectedItem().toString();
        try {
            clearModel();
            stmt = (Statement) dbUtility.con.createStatement();
            int count = 0;

            String sql = "select * from product left join groupfile on pgroup=groupcode where " + where + "  like '%" + code + "%' and pactive='Y'  "
                    + "order by PCode ";

            if (!record.equals("All")) {
                sql += "limit " + record;
            }

            ResultSet rs = stmt.executeQuery(sql);
            while (rs.next()) {
                count++;

                model.addRow(new Object[]{
                    chk(scode, procode, rs.getString("pcode")),
                    cvth.ASCII2Unicode(rs.getString("pgroup") + " " + rs.getString("groupname")),
                    cvth.ASCII2Unicode(rs.getString("pcode")),
                    cvth.ASCII2Unicode(rs.getString("pdesc")),
                    rs.getDouble("pprice11"),
                    cvth.ASCII2Unicode(rs.getString("punit1")),
                    cvth.ASCII2Unicode(rs.getString("pactive"))
                });
            }
            rs.close();
            stmt.close();
            lbllist.setText(count + "");
            tbltable.requestFocus();
            cleartxt();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, e);
        }
    }

