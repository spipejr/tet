    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        String inFile = chkpath + "/spapplication/dbconfig/language/font.ini";
        String outFile = chkpath + "/spapplication/dbconfig/languageconvert/font.ini";
        chkos = System.getProperty("os.name").toString().toLowerCase();
        String inCharset = "UTF-8";
        String outCharset = "TIS-620";
        String ArrayStr[] = new String [4];
        String sizefont[] = new String [4];
        String fontname [] = new String[4];
        SetupLanguage su = new SetupLanguage(new NimbusFrame(), true);
       if (!chkos.equalsIgnoreCase("linux")) {
        su.ConverCharSet(inFile, inCharset, outFile, outCharset);
        } else {
            su.copyFile(inFile, outFile);
        }
        File fObject = new File(outFile);
        if (!fObject.canRead()) {
            JOptionPane.showMessageDialog(null, "กรุณากำหนด File font.ini");
            System.exit(0);
        } else {
            try {
                boolean eof = false;
                FileReader file = new FileReader(outFile);
                BufferedReader buff = new BufferedReader(file);
                while (!eof) {
                    String line = buff.readLine();
                    if (line == null) {
                        eof = true;
                    } else {
                        if (!line.equals("")) {
                            String TempStr = line;
                            ArrayStr = TempStr.split(":");

                           
                        }
                    }
                }
                for(int i = 0;i<ArrayStr.length;i++){
                int strleng = ArrayStr[i].length()-2;
                 sizefont[i] = ArrayStr[i].substring(strleng);
                 fontname[i] = ArrayStr[i].substring(0,strleng);
               // System.out.print(fontname[i]+sizefont[i]);
                }
                buff.close();

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        dfont.setText(fontname[0]);
        dsize.setText(sizefont[0]);
        tfont.setText(fontname[1]);
        tsize.setText(sizefont[1]);
        efont.setText(fontname[2]);
        esize.setText(sizefont[2]);
        lfont.setText(fontname[3]);
        lsize.setText(sizefont[3]);
                
      
    }//GEN-LAST:event_formWindowOpened

