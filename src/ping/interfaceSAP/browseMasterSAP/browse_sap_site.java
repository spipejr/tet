    private void clearForm() {
        us.resetTable(model);
        us.closeTextFiled(txt_total, true);
        us.openTextFiled(txt_code, true, false);
        us.openTextFiled(txt_name, true, false);
        
        _site = new site();
        _site.setHaveSite(false);
        ArrayList<site> list = _site.seek();
        for (int i=0 ; i<list.size() ; i++) {
            model.addRow(new Object[]{
                (list.get(i).getSap_type().equals("FACTORY") ? "F" : "B"),
                list.get(i).getSap_code(),
                list.get(i).getSap_name(),
                list.get(i).getSap_costcenter(),
                list.get(i).getSap_address(),
                list.get(i).getSap_locality(),
                list.get(i).getSap_site_cashbank()
            });
        }
        txt_total.setText("รายการทั้งหมด " + list.size() + " รายการ");
        if (list.size() > 0) {
            jTable1.setFocusable(true);
            jTable1.setRowSelectionInterval(0, 0);
            jTable1.requestFocus();
        }
    }

    private void search() {
        us.resetTable(model);
        us.closeTextFiled(txt_total, true);
        
        String code = txt_code.getText().trim();
        String name = txt_name.getText();
        String data = (name.length() > 0 ? name : code);
        String table = (name.length() > 0 ? "sap_site_name" : "sap_site_code");
        _site = new site();
        ArrayList<site> list = _site.seek(data, table);
        for (int i=0 ; i<list.size() ; i++) {
            model.addRow(new Object[]{
                (list.get(i).getSap_type().equals("FACTORY") ? "F" : "B"),
                list.get(i).getSap_code(),
                list.get(i).getSap_name(),
                list.get(i).getSap_costcenter(),
                list.get(i).getSap_address(),
                list.get(i).getSap_locality(),
                list.get(i).getSap_site_cashbank()
            });
        }
        txt_total.setText("รายการทั้งหมด " + list.size() + " รายการ");
        if (list.size() > 0) {
            jTable1.setFocusable(true);
            jTable1.setRowSelectionInterval(0, 0);
            jTable1.requestFocus();
        }
    }

    private void pickupData() {
        int row = jTable1.getSelectedRow();
        String strSapType = "";
        if(jTable1.getValueAt(row, 0).equals("B")){
            strSapType  ="BRANCH";
        }else{
            strSapType = "FACTORY";
        }
        _site = new site("", jTable1.getValueAt(row, 1).toString(), jTable1.getValueAt(row, 2).toString(), strSapType, jTable1.getValueAt(row, 3).toString(), jTable1.getValueAt(row, 4).toString(), jTable1.getValueAt(row, 5).toString(), jTable1.getValueAt(row, 6).toString());
        _site.setHaveSite(true);
        dispose();
    }
