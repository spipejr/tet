    private void setParamiter() {
        try {
            report_veiw_sapproduct para = new report_veiw_sapproduct();
            if (!t_plu1.getText().trim().equals("")) {
                para.product1 = t_plu1.getText();
            } else {
                para.product1 = "";
            }

            if (!t_plu2.getText().trim().equals("")) {
                para.product2 = t_plu2.getText();
            } else {
                para.product2 = "ZZZZ";
            }

            if (!t_pgroup1.getText().trim().equals("")) {
                para.pgroup1 = t_pgroup1.getText();
            } else {
                para.pgroup1 = "";
            }

            if (!t_pgroup2.getText().trim().equals("")) {
                para.pgroup2 = t_pgroup2.getText();
            } else {
                para.pgroup2 = "ZZZZ";
            }

            if (p1.isSelected()) {
                para.cpctive = "1";
            } else if (p2.isSelected()) {
                para.cpctive = "2";
            } else {
                para.cpctive = "3";
            }

            if (s1.isSelected()) {
                para.cmapsap = "1";
            } else if (s2.isSelected()) {
                para.cmapsap = "2";
            } else {
                para.cmapsap = "3";
            }

            if (!t_btype1.getText().trim().equals("")) {
                para.blink1 = t_btype1.getText();
            } else {
                para.blink1 = "";
            }

            if (!t_btype2.getText().trim().equals("")) {
                para.blink2 = t_btype2.getText();
            } else {
                para.blink2 = "ZZZZ";
            }

        } catch (Exception e) {
        }

    }
